    private void adicionar() {
        String sql = "insert into fornecedores(nome,cnpj,telefone,email) values(?,?,?,?)";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtFornNome.getText());
            pst.setString(2, txtFornCnpj.getText());
            pst.setString(3, txtFornFone.getText());
            pst.setString(4, txtFornEmail.getText());

            if (txtFornNome.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatorios");
            } else {

                int adiciona = pst.executeUpdate();

                if (adiciona > 0) {
                    JOptionPane.showMessageDialog(null, "Fornecedor cadastrado");
                    txtFornNome.setText(null);
                    txtFornCnpj.setText(null);
                    txtFornFone.setText(null);
                    txtFornEmail.setText(null);

                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void pesquisa_fornecedor() {
        String sql = "select * from fornecedores where nome like ?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtFornPesquisar.getText() + "%");
            rs = pst.executeQuery();
            tblFornecedores.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
        }

    }

    public void seta_campos() {
        int seta = tblFornecedores.getSelectedRow();
        txtFornId.setText(tblFornecedores.getModel().getValueAt(seta, 0).toString());
        txtFornNome.setText(tblFornecedores.getModel().getValueAt(seta, 1).toString());
        txtFornCnpj.setText(tblFornecedores.getModel().getValueAt(seta, 2).toString());
        txtFornFone.setText(tblFornecedores.getModel().getValueAt(seta, 3).toString());
        txtFornEmail.setText(tblFornecedores.getModel().getValueAt(seta, 4).toString());
    }

    private void alterar() {
        String sql = "update fornecedores set nome=?, cnpj=?, telefone=?, email=? where id=?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtFornNome.getText());
            pst.setString(2, txtFornCnpj.getText());
            pst.setString(3, txtFornFone.getText());
            pst.setString(4, txtFornEmail.getText());

            pst.setString(5, txtFornId.getText());

            if (txtFornNome.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatorios");
            } else {

                int adiciona = pst.executeUpdate();

                if (adiciona > 0) {
                    JOptionPane.showMessageDialog(null, "Dados alterados");
                    txtFornNome.setText(null);
                    txtFornCnpj.setText(null);
                    txtFornFone.setText(null);
                    txtFornEmail.setText(null);

                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

